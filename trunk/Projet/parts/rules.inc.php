    <p id="intro">Les règles du jeu "Coyote believes he can fly" sont les suivantes.<p>

    <h4 id="but">1.1  Le but du jeu</h4>
    <p>Il s'agit de retrouver un mot et sa tratuction en anglais choisi aléatoirement par l'ordinateur. Le joueur peut faire un nombre fini de propositions.</p>

    <h4 id="deroulement">1.2  Le déroulement du jeu</h4>
    <p>L'odinateur choisit un mot et sa tratuction au hasard dans le dictionnaire. C'est ces mots qui seront les mots à trouver par le joueur. Le joueur fait une proposition. Tant que les conditions de fin de partie ne sont pas atteintes, le joueur peut faire une nouvelle proposition.</p>

    <h4 id="proposition">1.3  Une proposition</h4>
    <p>Avant chaque proposition, on indique le nombre de lettres du mot à trouver, les lettres déjà découvertes sont affichées à leur place, et les lettres qu'on doit encore découvrir sont symbolisées par des tiret bas '_'.<br/>
    <br/>
    Le joueur propose une lettre. Si celle-ci appartient au mot (anglais ou français), toutes ses occurences sont découvertes.<br/>
    Dans le cas contraire, la chutte de Coyote se poursuit.<br/>
    <br/>
    Avant la première proposition, la chutte de Coyote n'a pas encore commencé.<br/>
    <br/>
    Un indicateur informe le joueur de combien d'erreurs il dispose encore avant de perdre.

    <h4 id="fin">1.4  Fin de partie</h4>
    <p>la partie se termine lorsque :</p>
    <ul>
        <li>Coyote s'écrase au sol. Le joueur a alors perdu.</li>
        <li>Le joueur a découvert les mots à trouver. Dans ce cas, le joueur a gagné.</li>
    </ul>
